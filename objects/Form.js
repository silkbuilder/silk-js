	/**
	 * Retursn the form inputs' values as an JavaScript Object's properties.
	 * @returns {Object} - An Oject with the form's inputs as properties
	 */
	this.getObject = function() {
		let returnObject = {};
		for (let item of this.getOperationList()[0].columnList) {
			returnObject[item.column] = item.value;
		}
		return returnObject;
	};

	/**
	 * Return the form input's values as a JSON String.
	 * @returns {JSON} - A JSON String 
	 */
	this.getJSON = function() {
		return JSON.stringify(this.getObject());
	};

	/**
	 * Excutes a jQuesr ajax post/json call to the provided URL. The handle function received the parameter result, which can be true or false, and the response data.
	 * @param {String} serviceURL - The URL to the target service
	 * @param {Function} handleFunction - The functin which will received the response.
	 */
	this.post = function(serviceURL, handleFunction) {
		postToService(serviceURL, this.getObject(), handleFunction, this);
	};

