	this.setDelete = function() {
		if (this.getAction() != "none") return;
		if (pageID != undefined) window[pageID].show();
		this.setAction("delete");
		this.setMode(true);
	}

	/**
	 * Operates a cancel operation. This operation is executed when clicking the form's cancel button, if set.
	 * @param {Boolean} buttonClicked - Indicates that the call is from a button being clicked. Only use interanlly.
	 */
	this.doCancel = function(buttonClicked) {
		if (!buttonClicked) buttonClicked = false;
		if (this.getAction() == "none") return;
		// Any action leads to close the form and return to parent list
		// If the page is modal it closes after an insert and delete.
		if (pageID != undefined) {
			if (!isModal || !buttonClicked) window[pageID].goBack();
		}
		this.setAction("none");
		this.setMode(false);
		this.fillInputValues();
		this.setInputMode(false);

	}


	this.validate = function() {
		var inputsErrors = new Array();
		for (x in inputs) {
			if (this[inputs[x]].validate() > 0) {
				inputsErrors.push(this[inputs[x]].getLabel());
			}
		}
		return inputsErrors;
	}

	/**
	 * Operates the data submission process. This operation is executed when clicking the form's submit button, if set.
	 * @param {String} action - The action to be executed.
	 */
	this.doSubmit = function(action) {
		/*
		 * Stop if action is equals to "none" 
		 */
		if (this.getAction() == "none" && dataSource != null) return;

		/**
		 * This event is triggered before the form's data is validated for submission. Use this event to apply changes or run operations before the form's input validation. If it returns false, the process is canceled. Created with the ```Form.on("beforeSubmit", function(){})``` method.
		 * @event Form#Event:beforeSubmit
		 */
		if (!ifUndefined(eventManager.dispatch("beforeSubmit"), true)) return;

		/*
		 * Collects and counts input errors.
		 */
		var inputsErrorReported = this.validate();

		if (inputsErrorReported.length == 0) {
			/*
			 * No errors found
			 */

			/**
			 * This event is triggered before the form's data has been validated and is ready to be submitted for processing. The event *beforeSubmit* is triggered before this event.
			 * If the function returns *false* the process is canceled. Created with the ```Form.on("submit", function(){})``` method.
			 * @event Form#Event:submit
			 */
			if (!ifUndefined(eventManager.dispatch("submit"), true)) return;

			/*
			 * Submits the form's data fo the dataSource.
			 * If the transaction is sucessful the form mode will change to read-only.
			 */
			if (dataSource != null) {
				/*
				 * Disables form's inputs and buttons before submitting.
				 */
				this.setDisable();

				/*
				 * Calls DataProvider action.
				 */
				dataSource.callAction(this.getOperationList(this.getAction()));
			}
		} else {

			/*
			 * Errors found
			 */

			/**
			 * This event is triggered when the form's inputs have errors.  It is created with the ```Form.on("formErrors", function(){})``` method. The function receives an array containing the labels of the inputs needing correction.
			 * When data has to be corrected, the form's inputs will show a warning border color. The developer can use this event to add extra guiding messages.
			 * @param {Array} inputList - The list of iputs with errors.
			 * @event Form#Event:formErrors
			 */
			eventManager.dispatch("formErrors", inputsErrorReported);
		}
	}

