	if (window[id + "_submitBt"] != undefined) this.submitBt = window[id + "_submitBt"];

	/*
	 * Function to initialize jQuery related values and events
	 */
	this.initForm = function() {

		// Form HTML element
		this.$form = $("#" + id);

		// The HTML form dialog bar
		$formBar = $("#" + id + "_dialogBar");
		$formBarTitle = $("#" + id + "_dialogTitle");

		// Cancel and Submit labels
		if (isNotEmpty(window[id + "_cancelBt"])) window[id + "_cancelBt"].setLabel(cancelBtLabel);
		if (isNotEmpty(window[id + "_submitBt"])) window[id + "_submitBt"].setLabel(submitBtLabel);

		if ($formBar.length == 0) $formBar = null;

		for (x in inputs) {
			this[inputs[x]].initInput();
		}

		//Base on the first input defines the pageID and moves the dialog bar to the page bar.
		if (inputs.length > 0 && pageID == undefined) {
			pageID = this[inputs[0]].$input.closest(".silk-page").attr("id");

			if (pageID == undefined) {
				pageID = this[inputs[0]].$input.closest(".modal").attr("id");
				if (pageID != undefined) {
					isModal = true;
				}
			}

			if (pageID != undefined) {
				$pageBar = window[pageID].$page.find(".silk-navbar");
				var $topBar = window[pageID].$page.find(".silk-top-bar");

				if ($topBar.length != 0) {
					if ($formBar != null) $topBar.append($formBar);
				}
				$("#" + pageID + "_closeBt").prependTo($("#" + pageID + "_closeBt").parent());
			}

		}

		/*
		 * Sets the insert button click method to the setInsert method.
		 * Cancel the process if the onInsert event returns false
		 */
		if (this.insertBt != null) {
			this.insertBt.on("click", $.proxy(function() {

				/**
				 * This event is triggered before the form is set for an insert. If the function returns *false*, the process is canceled. Created with the ```Form.on("insert", function(){})``` method.
				 * @event Form#Event:insert
				 */
				if (!ifUndefined(eventManager.dispatch("insert"), true)) return;

				this.setInsert();

			}, this));
		}

		/*
		 * Sets the update button click method to the setUpdate method.
		 * Cancel the process if the onUpdate event returns false
		 */
		if (this.updateBt != null) {
			this.updateBt.on("click", $.proxy(function() {

				/**
				 * This event is triggered before the form is set for an update. If the function returns *false* the process is canceled. Created with the ```Form.on("update", function(){})``` method.
				 * @event Form#Event:update
				 */
				if (!ifUndefined(eventManager.dispatch("update"), true)) return;

				this.setUpdate();

			}, this));
		}

		/*
		 * Sets the delete button click method to the setDelete method.
		 * Cancel the process if the onDelete event returns false
		 */
		if (this.deleteBt != null) {
			this.deleteBt.on("click", $.proxy(function() {

				/**
				 * This event is triggered before the form is set for a delete. If the function returns *false* the process is canceled. Created with the ```Form.on("delete", function(){})``` method.
				 * @event Form#Event:delete
				 */
				if (!ifUndefined(eventManager.dispatch("delete"), true)) return;

				this.setDelete();

			}, this));
		}

		/*
		 * Sets the cancel button click method to the doCancel method.
		 * Cancel the process if the onCancel event returns false
		 */
		if (this.cancelBt != null) {
			this.cancelBt.on("click", $.proxy(function() {

				/**
				 * This event is triggered before the form executes a cancel. If the function returns *false* the process is canceled. Created with the ```Form.on("cancel", function(){})``` method.
				 * @event Form#Event:cancel
				 */
				if (!ifUndefined(eventManager.dispatch("cancel"), true)) return;

				/*
				 * Calls doCancel. The parameter indicate that a button was clicked.
				 */
				this.doCancel(true);

			}, this));
		}

		/*
		 * Sets the confirmation dialog properties.
		 */
		if (confirmTitle != "") this.submitBt.setConfirmTitle(confirmTitle);
		if (confirmMessage != "") this.submitBt.setConfirmMessage(confirmMessage);
		if (confirmLabel != "") this.submitBt.setConfirmLabel(confirmLabel);
		if (confirmIcon != "") this.submitBt.setConfirmIcon(confirmIcon);

		if (isNotEmpty(confirmTarget)) {

			this.submitBt.on("validateConfirm", function() {
				return confirmTarget.indexOf(_action) > -1;
			});
		}

		/*
		 * Sets the submit button click method to the doSubmit method.
		 * Cancel the automatic submit process if the onSubmit event returns false
		 */
		if (this.submitBt != null) {
			this.submitBt.on("click", $.proxy(function() {
				this.doSubmit();
			}, this));
		}

		if (startMode) {
			setTimeout($.proxy(function() {
				this.setMode(true);
				this.setAction("update");
			}, this), 400);
		}

	}

